    private void sales() 
    {
        DateTimeFormatter dtf = DateTimeFormatter.ofPattern("yyyy/MM/dd");       
        LocalDateTime now = LocalDateTime.now();
        String date = dtf.format(now);
        String cashier = jLabel14.getText();
        double subtot = Double.parseDouble(txtsub.getText());
        double pay = Double.parseDouble(txtpay.getText());
        double bal = Double.parseDouble(txtbal.getText());
        String CustName = txtCustName.getText();
        String CustAdd = txtCustAdd.getText();
        String CustContact = txtContact.getText();
        int prod = txtprodcode.getSelectedIndex();
        int lastinsertid = 0;
        
        
       
        try {
            Class.forName("com.mysql.jdbc.Driver");
            conn = DriverManager.getConnection("jdbc:mysql://localhost/inventorymanagementsystem","root","Naneeta1");
            String query = "insert into sales (date,cashier,subtotal,pay,balance,custname)values(?,?,?,?,?,?)";
            insert = conn.prepareStatement(query, Statement.RETURN_GENERATED_KEYS);
            insert.setString(1, date);
            insert.setString(2, cashier);
            insert.setDouble(3, subtot);
            insert.setDouble(4, pay);
            insert.setDouble(5, bal);
            insert.setString(6, CustName);
            insert.executeUpdate();
            
            
            ResultSet generatedKeyResult = insert.getGeneratedKeys();
            if(generatedKeyResult.next())
            {
                lastinsertid = generatedKeyResult.getInt(1);
            }
            int rows = jTable1.getRowCount();
            
            String Query = "insert into customer (name,contact,Address,product_id,user)values(?,?,?,?,?)";
            insert = conn.prepareStatement(Query, Statement.RETURN_GENERATED_KEYS);
            insert.setString(1, CustName);
            insert.setString(2, CustContact);
            insert.setString(3, CustAdd);
            insert.setInt(4, prod);
            insert.setString(5, cashier);
            insert.executeUpdate();
            
            
            String query1 = "insert into sales_product (sales_id,product_id,sell_price,qty,total)values(?,?,?,?,?)";
            insert = conn.prepareStatement(query1);
            String product_id ="";
            double price =0;
            int  qty =0;
            double total = 0;
            for(int i=0; i<jTable1.getRowCount(); i++)
            {
                product_id = (String)jTable1.getValueAt(i, 0);
                price = (double)jTable1.getValueAt(i, 1);
                qty = Integer.parseInt((String)jTable1.getValueAt(i, 2));
                total = (double)jTable1.getValueAt(i, 3);
                
                insert.setInt(1, lastinsertid);
                insert.setString(2, product_id);
                insert.setDouble(3, price);
                insert.setInt(4, qty);
                insert.setDouble(5, total);
                insert.executeUpdate();
                
            }
            
            String query3 = "update product set quantity = quantity-? where prd=?";
            insert = conn.prepareStatement(query3);
            
            for(int i=0; i<jTable1.getRowCount(); i++)
            {
                product_id = (String)jTable1.getValueAt(i, 0);
                
                qty = Integer.parseInt((String)jTable1.getValueAt(i, 2));
                insert.setString(2, product_id);
                insert.setInt(1, qty);
                insert.execute();
                
            }
                     
            insert.addBatch();
            JOptionPane.showMessageDialog(this, "Record Saved");
            txtCustName.setText("");
            txtContact.setText("");
            txtCustAdd.setText("");
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
    
   
    
    
    
    

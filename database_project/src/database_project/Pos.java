    private void pos()
    {
        
        
            String name = (String)txtprodcode.getSelectedItem();
            try {
                Class.forName("com.mysql.jdbc.Driver");
            } catch (ClassNotFoundException ex) {
                Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
            }
            try {
                conn = DriverManager.getConnection("jdbc:mysql://localhost/inventorymanagementsystem","root","Naneeta1");
                insert = conn.prepareStatement("select * from product where prd=?");
                insert.setString(1, name);
                rs = insert.executeQuery();
                if(rs == null){
                    JOptionPane.showMessageDialog(this, "This Product doesn't exists ");
                }
                
                while(rs.next())
                {
                    int currentqty;
                    
                    currentqty = rs.getInt("quantity");
                    double price = rs.getInt("retail_price");
                    int newqty = Integer.parseInt(txtqty.getText());
                    double tot = (price * newqty);
                    if(newqty >= currentqty)
                    {
                        JOptionPane.showMessageDialog(this, "Available Product = " + currentqty);
                        JOptionPane.showMessageDialog(this, "Quantityy is not Enough");
                    }
                    else
                    {
                        model = (DefaultTableModel)jTable1.getModel();
                        model.addRow(new Object[]
                        {
                            txtprodcode.getSelectedItem(),
                            price,
                            txtqty.getText(),
                            tot,
                        });
                        
                        int sum = 0;
                        for(int i = 0; i< jTable1.getRowCount(); i++)
                        {
                            sum = sum + Integer.parseInt(jTable1.getValueAt(i, 2).toString());
                            
                        }
                        
                        
                        total = total+tot; 
                        txtsub.setText(Double.toString(total)); 
                       
                                
                                
                        txtprodcode.setSelectedIndex(-1);
                        txtqty.setText("");     
                        txtprodcode.requestFocus();

                        
                    }
                }
        
             }   catch (SQLException ex) {
            Logger.getLogger(Pos.class.getName()).log(Level.SEVERE, null, ex);
            }
    }
     
